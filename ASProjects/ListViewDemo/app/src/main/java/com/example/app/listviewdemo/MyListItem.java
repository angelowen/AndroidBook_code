package com.example.app.listviewdemo;

public class MyListItem {
    private int img;
    private String wonders;
    private String wondersURL;

    public void setImg(int img)
    {
        this.img = img;
    }

    public  int getImg()
    {
        return this.img;
    }

    public void setWonders(String wonders)
    {
        this.wonders = wonders;
    }

    public String getWonders()
    {
        return this.wonders;
    }

    public void setWondersURL(String wondersURL)
    {
        this.wondersURL = wondersURL;
    }

    public String getWondersURL()
    {
        return  this.wondersURL;
    }
}
